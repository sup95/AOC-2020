    for(element in pairList) {
        var treeCount = 0
        var i = 0
        var j = 0
        while(i in 0..inputSize) {
            if((i + element.second) >= inputSize) break
            if((j + element.first) >= rowLength) {
                val x = rowLength - 1 - j
                j = element.first - x -1
            }
            else 
                j += element.first
            if(inputArray[i + element.second][j] == '#')	treeCount++

            i += element.second
        }
        println(treeCount)
        overallTreeCount *= treeCount
    }
    
